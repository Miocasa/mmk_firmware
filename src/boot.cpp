bool bootAnimation(Adafruit_SSD1306 *display) {
    static uint32_t time = millis();
	const uint32_t now = millis();

    static uint8_t frame = 0;
    if (frame >= boot_animation_frames_count + 10) {
        return true;
    }

    if (now - time > BOOT_TIMEOUT) {
    	if (frame >= boot_animation_frames_count) {
    		frame++;
    		return false;
    	}

        display->clearDisplay();

    	display->drawBitmap(
			(display->width() - boot_animation_width) / 2,
			(display->height() - boot_animation_height) / 2,
			boot_animation_allFrames[frame],
			boot_animation_width,
			boot_animation_height,
			WHITE
		);
    	constexpr char text[] = "Hello Mio";

    	display->setTextColor(WHITE);
    	display->setCursor(0,6);
    	display->setFont(&Picopixel);

    	const uint8_t textLen = (frame >= sizeof(text) ? sizeof (text) :frame );
    	for (uint8_t i = 0; i < textLen; i++) {
			display->print(text[i]);
    	}

        display->display();
    	frame++;
    	time = now;
    }

    return false;
}
